<div class="left">
  <h4>A dynamic website using PHP and MySQL to demonstrate an online shop</h4>
  <p>
    HyperAV was a group project that was built for the 2nd year Information Systems and Database Development assignment. The aim for the project was to design and build a database with a PHP front end that would be the online presence for a fictional audio visual store, that has multiple store locations.
  </p>
