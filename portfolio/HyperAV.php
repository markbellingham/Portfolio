    <h3>Website features:</h3>
    <ul>
      <li>List products, filter by category</li>
      <li>View individual product with extended information and related products</li>
      <li>Register and login system</li>
      <li>Automatically updating shopping basket with indicator</li>
      <li>User can change quantity and delete items in basket</li>
      <li>Customer can view previous orders</li>
      <li>User can change their password</li>
      <li>Admin can insert update and delete items</li>
      <li>Admin can view various reports</li>
